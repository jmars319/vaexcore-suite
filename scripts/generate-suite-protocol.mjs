#!/usr/bin/env node
import { existsSync, mkdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { loadSuiteConfig, suiteRoot } from "./lib/suite-config.mjs";

const checkOnly = process.argv.includes("--check");
const config = loadSuiteConfig();
const files = new Map([
  [join(suiteRoot, "suite/generated/suite-protocol.ts"), renderTypeScriptProtocol()],
  [join(suiteRoot, "console/VaexCore/desktop/shared/src/suiteProtocol.ts"), renderTypeScriptProtocol()],
  [join(suiteRoot, "studio/apps/desktop/src-tauri/src/suite_protocol.rs"), renderRustProtocol()],
  [join(suiteRoot, "pulse/apps/desktopapp/src-tauri/src/suite_protocol.rs"), renderRustProtocol()],
]);

const mismatches = [];
for (const [path, contents] of files) {
  if (checkOnly) {
    if (!existsSync(path) || readFileSync(path, "utf8") !== contents) {
      mismatches.push(path);
    }
    continue;
  }
  mkdirSync(dirname(path), { recursive: true });
  writeFileSync(path, contents);
  console.log(`generated ${path}`);
}

if (mismatches.length > 0) {
  for (const path of mismatches) {
    console.error(`error: generated suite protocol is out of date: ${path}`);
  }
  process.exit(1);
}

if (checkOnly) {
  console.log("generated suite protocol is current");
}

function renderTypeScriptProtocol() {
  const apps = config.apps.map((app) => ({
    id: app.id,
    name: app.name,
    bundleId: app.bundleId,
    launchName: app.launchName,
    discoveryFile: app.discoveryFile,
    healthEndpoint: app.healthEndpoint,
    capabilities: app.capabilities,
  }));
  return `${generatedHeader("//")}
export const SUITE_DISCOVERY_SCHEMA_VERSION = ${config.contract.discovery.schemaVersion} as const;
export const PULSE_RECORDING_INTAKE_FILE = ${js(config.contract.handoffs.pulseRecordingIntakeFile)} as const;
export const MARKER_CONTRACT_NAME = ${js(config.contract.markerContract.name)} as const;
export const MARKER_CONTRACT_SCHEMA_VERSION = ${config.contract.markerContract.schemaVersion} as const;
export const MARKER_REQUIRED_METADATA_FIELDS = ${js(config.contract.markerContract.requiredMetadataFields)} as const;

export const SUITE_APPS = ${js(apps)} as const;
export const STUDIO_APP = SUITE_APPS.find((app) => app.id === "vaexcore-studio")!;
export const PULSE_APP = SUITE_APPS.find((app) => app.id === "vaexcore-pulse")!;
export const CONSOLE_APP = SUITE_APPS.find((app) => app.id === "vaexcore-console")!;
export const SUITE_APP_IDS = SUITE_APPS.map((app) => app.id);
export const SUITE_LAUNCH_NAMES = SUITE_APPS.map((app) => app.launchName);

export type SuiteAppId = (typeof SUITE_APPS)[number]["id"];
`;
}

function renderRustProtocol() {
  const apps = config.apps;
  return `${generatedHeader("//")}#![allow(dead_code)]

pub const SUITE_DISCOVERY_SCHEMA_VERSION: u8 = ${config.contract.discovery.schemaVersion};
pub const PULSE_RECORDING_INTAKE_FILE: &str = ${rs(config.contract.handoffs.pulseRecordingIntakeFile)};
pub const MARKER_CONTRACT_NAME: &str = ${rs(config.contract.markerContract.name)};
pub const MARKER_CONTRACT_SCHEMA_VERSION: u8 = ${config.contract.markerContract.schemaVersion};
pub const STUDIO_APP_ID: &str = ${rs(apps[0].id)};
pub const PULSE_APP_ID: &str = ${rs(apps[1].id)};
pub const CONSOLE_APP_ID: &str = ${rs(apps[2].id)};
pub const VAEXCORE_SUITE_APPS: &[&str] = &[${apps.map((app) => rs(app.launchName)).join(", ")}];

#[derive(Clone, Copy)]
pub struct SuiteAppDefinition {
    pub app_id: &'static str,
    pub app_name: &'static str,
    pub launch_name: &'static str,
    pub bundle_identifier: &'static str,
    pub discovery_file: &'static str,
    pub health_endpoint: &'static str,
}

pub const SUITE_APP_DEFINITIONS: &[SuiteAppDefinition] = &[
${apps
  .map(
    (app) => `    SuiteAppDefinition {
        app_id: ${rs(app.id)},
        app_name: ${rs(app.name)},
        launch_name: ${rs(app.launchName)},
        bundle_identifier: ${rs(app.bundleId)},
        discovery_file: ${rs(app.discoveryFile)},
        health_endpoint: ${rs(app.healthEndpoint)},
    },`,
  )
  .join("\n")}
];
`;
}

function generatedHeader(prefix) {
  return `${prefix} Generated by scripts/generate-suite-protocol.mjs. Do not edit by hand.\n\n`;
}

function js(value) {
  return JSON.stringify(value, null, 2);
}

function rs(value) {
  return JSON.stringify(value);
}
